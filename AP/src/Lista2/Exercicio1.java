/*
 * 1- Faça um programa que mostre todos os números inteiros de 1 a 50. 
 * Use o laço WHILE
 */
package Lista2;


public class Exercicio1 {
    public static void main(String[] args) {
        int i=1;
        while(i<=50){
            System.out.println(i);
            i=i+1;
        }
        
        
    }
    
}
